package entity.service;

/**
 * @author guohaohao
 */
public class WishSummary {

    /**
     * 抽卡总数
     */
    private int totalCount;
    /**
     * 抽到四星个数
     */
    private int starFourCount;
    /**
     * 抽到四星角色个数
     */
    private int starFourCharacterCount;
    /**
     * 抽盗四星武器个数
     */
    private int starFourWeaponCount;
    /**
     * 抽到五星个数
     */
    private int starFiveCount;
    /**
     * 抽到五星角色个数
     */
    private int starFiveCharacterCount;
    /**
     * 抽到五星武器个数
     */
    private int starFiveWeaponCount;

    public int getTotalCount() {
        return totalCount;
    }

    public void setTotalCount(int totalCount) {
        this.totalCount = totalCount;
    }

    public int getStarFourCount() {
        return starFourCount;
    }

    public void setStarFourCount(int starFourCount) {
        this.starFourCount = starFourCount;
    }

    public int getStarFourCharacterCount() {
        return starFourCharacterCount;
    }

    public void setStarFourCharacterCount(int starFourCharacterCount) {
        this.starFourCharacterCount = starFourCharacterCount;
    }

    public int getStarFourWeaponCount() {
        return starFourWeaponCount;
    }

    public void setStarFourWeaponCount(int starFourWeaponCount) {
        this.starFourWeaponCount = starFourWeaponCount;
    }

    public int getStarFiveCount() {
        return starFiveCount;
    }

    public void setStarFiveCount(int starFiveCount) {
        this.starFiveCount = starFiveCount;
    }

    public int getStarFiveCharacterCount() {
        return starFiveCharacterCount;
    }

    public void setStarFiveCharacterCount(int starFiveCharacterCount) {
        this.starFiveCharacterCount = starFiveCharacterCount;
    }

    public int getStarFiveWeaponCount() {
        return starFiveWeaponCount;
    }

    public void setStarFiveWeaponCount(int starFiveWeaponCount) {
        this.starFiveWeaponCount = starFiveWeaponCount;
    }
}
