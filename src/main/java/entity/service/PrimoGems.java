package entity.service;

/**
 * @author guohaohao
 */
public class PrimoGems {

    /**
     * 原石数
     */
    private Integer primogems;
    /**
     * 星辉数
     */
    private Integer starlight;
    /**
     * 树脂数
     */
    private Integer resin;

    public Integer getPrimogems() {
        return primogems;
    }

    public void setPrimogems(Integer primogems) {
        this.primogems = primogems;
    }

    public Integer getStarlight() {
        return starlight;
    }

    public void setStarlight(Integer starlight) {
        this.starlight = starlight;
    }

    public Integer getResin() {
        return resin;
    }

    public void setResin(Integer resin) {
        this.resin = resin;
    }
}
